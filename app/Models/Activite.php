<?php

class Activite
{
    protected $idActivite;
    protected $nom;
    protected $prenom;
    protected $email;
    protected $dateIscription;

    function __construct($idActivite, $nom, $prenom, $email,$dateIscription) 
    {
        $this.$idActivite->$idActivite;
        $this.$nom->$nom;
        $this.$prenom->$prenom;
        $this.$email->$email;
        $this.$dateIscription->$dateIscription;
    }


}

?>